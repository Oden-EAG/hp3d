!**************************************************************************************************************************
      integer FUNCTION  my_mod(i,n)
!**************************************************************************************************************************
      !*** LATEST REVISION:    Aug 08
      !
      !*** PURPOSE:            function sets representative for equivalence class of 0 equal to 3; hence mod3(n) belongs 
      !                        to set {1,2,3} instead of {0,1,2}, which is the case for function mod(n,3).
      !
      !*** REQUIRE PROCEDURES: none
!**************************************************************************************************************************
      IMPLICIT NONE
!**************************************************************************************************************************
      !*** local variables
      integer, intent(in) :: i,n
!**************************************************************************************************************************      
      my_mod = mod(i,n)
      !
      if (my_mod .eq. 0) my_mod = n
      !
      END FUNCTION my_mod
